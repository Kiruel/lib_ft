int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int i2;

	i2 = 0;
	i = 0;
	while (*s1 != '\0')
	{
		s1++;
		i++;
	}
	while (*s2 != '\0')
	{
		s2++;
		i2++;
	}
	if (i < i2)
		return (-666);
	if (i == i2)
		return (0);
	if (i > i2)
		return (8);
	return (0);
}
