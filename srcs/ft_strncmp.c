int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;
	unsigned int i2;

	i2 = 0;
	i = 0;
	while (*s1 != '\0' && i != n)
	{
		s1++;
		i++;
	}
	while (*s2 != '\0' && i2 != n)
	{
		s2++;
		i2++;
	}
	return (i - i2);
}
